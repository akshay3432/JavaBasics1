public class Escapes {

    public static void main(String[]args ){
        System.out.println(" Hello \"Akshay\" ") ; //escape sequence for putting double commas
        System.out.println(" Hello \nAkshay ") ;//for next line
        System.out.println(" Hello \tAkshay ") ;//for tab or space
        System.out.println(" Hello\bAkshay ") ;



    }
}
